function swapvalues(value1, value2) {
  console.log("Before swap:", value1, value2);
  var temp = value1;
  value1 = value2;
  value2 = temp;
  console.log("After swap:", value1, value2);
}
swapvalues("Virat", "Anushka");
swapvalues(1000, 2000);
